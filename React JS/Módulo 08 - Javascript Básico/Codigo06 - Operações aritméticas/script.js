
var a = 10;
var b = 15;

// + Adição
var c = a + b;
console.log(c);

// - Subtração
var d = a - b;
console.log(d);

// * Multiplicação
var e = a * b;
console.log(e);

// / Divisão
var f = a / b;
console.log(f);

// % Resto da divisão
var g = a % b;
console.log(g);

// ++ Incremento
a++; // a = a + 1
console.log(a);

h = a++; // h = a; a = a + 1
console.log(h);

h = ++a; // h = a + 1
console.log(h);

// -- Decremento
a--; // a = a - 1
console.log(a);

j = a--; // j = a; a = a - 1
console.log(j);

j = --a; // j = a - 1
console.log(j);

