// FOR

var numero1 = 5;
for(var i = 0; i < numero1; i++){
    console.log('Vez: ' + i);
}
console.log('Fim');

// WHILE
var numero2 = 5;
while(numero2 < 10){
    console.log('Vez: ' + numero2);
    numero2++;
}
console.log('Fim');

// DO WHILE
var numero3 = 5;
do{
    console.log('Vez: ' + numero3);
    numero3++;
} while(numero3 < 10);

console.log('Fim');
