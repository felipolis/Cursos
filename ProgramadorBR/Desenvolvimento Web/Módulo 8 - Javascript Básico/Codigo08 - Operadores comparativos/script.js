// == igual a
console.log(2 == 3);
console.log(2 == 2);
console.log(2 == '2');

// === igual e identico
console.log(2 === 3);
console.log(2 === 2);
console.log(2 === '2');

// != diferente
console.log(2 != 3);
console.log(2 != 2);
console.log(2 != '2');

// !== diferente e identico
console.log(2 !== 3);
console.log(2 !== 2);
console.log(2 !== '2');

// > maior que
console.log(2 > 3);
console.log(2 > 2);
console.log(2 > 1);

// < menor que
console.log(2 < 3);
console.log(2 < 2);
console.log(2 < 1);

// >= maior ou igual
console.log(2 >= 3);
console.log(2 >= 2);
console.log(2 >= 1);

// <= menor ou igual
console.log(2 <= 3);
console.log(2 <= 2);
console.log(2 <= 1);
